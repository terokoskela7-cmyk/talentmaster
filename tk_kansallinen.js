// Hae seuran ranking ikäluokassa: palauttaa sijoituksen ja prosenttipisteen
function tkHaeSeuranSijoitus(seura, kausi, suku, ika) {
  var key = kausi + '_' + suku + '_' + ika;
  var dist = TK_KANSALLINEN.ik_dist[key];
  if (!dist || !dist.length) return null;
  var oma = dist.find(function(d) { return d.seura === seura; });
  if (!oma) return null;
  var sijoitus = dist.indexOf(oma) + 1;
  var pp = Math.round((dist.length - sijoitus) / dist.length * 100);
  return { sijoitus: sijoitus, n: dist.length, pp: pp, pct: oma.pct, oma: oma };
}

// Hae kansallinen top-N listana
function tkHaeTopN(kausi, suku, ika, n) {
  var key = kausi + '_' + suku + '_' + ika;
  var dist = TK_KANSALLINEN.ik_dist[key] || [];
  return dist.slice(0, n || 10);
}

// Laske kansallinen mitaliprosentti-mediaani ikäluokassa
function tkKansallinenMediaani(kausi, suku, ika) {
  var key = kausi + '_' + suku + '_' + ika;
  var dist = TK_KANSALLINEN.ik_dist[key] || [];
  if (!dist.length) return null;
  var sorted = dist.slice().sort(function(a,b){return b.pct-a.pct;});
  var mid = Math.floor(sorted.length / 2);
  return sorted[mid].pct;
}

